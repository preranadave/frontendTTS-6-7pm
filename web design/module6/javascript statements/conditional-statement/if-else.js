/* if is executed when condition is true if condition is false else  

   syntax : 

      if(condition)
      {
        statements;
      }
      else 
      {
        statements
      }

*/

let a = 10;
let b = 25;
if (a > b) {
    console.log("a is greater than b")
}
else {
    console.log('a is less than b')
}